package com.sfu.entity;

public class Users {
	private int uid;
	private String uname;
	private String pwd;
	private String utel;
	int ucampus;
	public int getUid() {
		return uid;
	}
	public void setUid(int uid) {
		this.uid = uid;
	}
	public String getUname() {
		return uname;
	}
	public void setUname(String uname) {
		this.uname = uname;
	}
	public String getPwd() {
		return pwd;
	}
	public void setPwd(String pwd) {
		this.pwd = pwd;
	}
	public String getUtel() {
		return utel;
	}
	public void setUtel(String utel) {
		this.utel = utel;
	}
	public int getUcampus() {
		return ucampus;
	}
	public void setUcampus(int ucampus) {
		this.ucampus = ucampus;
	}
	
}
